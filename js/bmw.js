function displayToday() {
    var today = new Date();
    var day = today.getDate();
    var month = today.getMonth() + 1;
    var year = today.getFullYear();
    var formattedDate = day + '/' + month + '/' + year;
    document.getElementById("today").innerText = formattedDate;
}
