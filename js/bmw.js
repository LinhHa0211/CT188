function show(typeCar){
    for (var i = 0 ; i < Object.keys(productList).length; i++){
        document.getElementById("product-name-"+(i+1).toString()).innerText = productList[typeCar+(i+1).toString()]["product-name"];
        document.getElementById("price-"+(i+1).toString()).innerText = "Giá từ "+productList[typeCar+(i+1).toString()]["price"].toLocaleString('vi-VN')+"đ";
        document.getElementById("year-"+(i+1).toString()).innerText = productList[typeCar+(i+1).toString()]["year"];
    }
}
function showPhoto(id,list,item){
    var container = document.getElementById(id);
    container.innerHTML="";
    for (var i = item[list].length-3; i < item[list].length ; i++){
        var photoCell = document.createElement("div");
        if ( i != item[list].length-1 )
            photoCell.innerHTML=" <div class='owl-item' style='width: 30%; margin-right: 5%;'><div class='ct' ><img src='"+ item[list][i] +"' alt=''></div>";
        else
            photoCell.innerHTML=" <div class='owl-item cloned' style='width: 30%;'><div class='ct'><img src='"+ item[list][i] +"' alt=''></div>";
        container.appendChild(photoCell);
    }
}
