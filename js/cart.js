function setInput(code, value) {
    let product = {"code": code, "value": value};
    if(inputAt[typeCar] == []){
        inputAt[typeCar].push(product);
    }
    else {
        inputAt[typeCar][0] = product;
    }
}

function getCode() {
    return inputAt[typeCar][0].code;
}
function getValue() {
    return inputAt[typeCar][0].value;
}

