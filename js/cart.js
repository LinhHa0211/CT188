function getDiscountRate(typeOfVIP, isPersonal){    
    if(typeOfVIP == 'SILVER'){
        return isPersonal ? 0.01 : 0.05;
    }
    else if(typeOfVIP == 'GOLD'){
        return isPersonal ? 0.03 : 0.08;
    }
    else if(typeOfVIP == 'DIAMOND'){
        return isPersonal ? 0.05 : 0.1;
    }
    else return 0;
}

function showCart(typeOfVIP = 'NORMAL', isPersonal = true) {
    var formatter = new Intl.NumberFormat('vi-VN', {style: 'currency', currency: 'VND'});

    var container = document.getElementById('cart-detail').getElementsByTagName('tbody')[0];
    container.innerHTML = '';
    var sumPerProduct = 0;
    var totalPreTax = 0;
    var discountRate = getDiscountRate(typeOfVIP, isPersonal);
    var taxRate = 0.1;
    var discount = 0;
    var tax = 0;

    console.log(window.localStorage.length);
    for(let i = 0; i < window.localStorage.length; i++) {
        if(typeof productList[localStorage.key(i)] === 'undefined') {
            continue;
        }

        var tr = document.createElement("tr");
        var imageCell = document.createElement("td");
        var nameCell = document.createElement("td");
        var proYearCell = document.createElement("td");
        var numberCell = document.createElement("td");
        var priceCell = document.createElement("td");
        var sumCell = document.createElement("td");
        var removeCell = document.createElement("td");
        var removeLink = document.createElement("a");
        var purchaseCell = document.createElement("td");
        var purchaseBtn = document.createElement("button");
        
        var product = productList[window.localStorage.key(i)];
        var number = window.localStorage.getItem(window.localStorage.key(i));

        imageCell.style.textAlign = "center";
        imageCell.innerHTML = `<img src="${product["photo-main"]}">`;
        imageCell.classList.add("car-img");

        nameCell.style.textAlign = "center";
        nameCell.innerHTML = product["automaker"] + " " + product["product-name"];
        nameCell.classList.add("car-name");

        proYearCell.style.textAlign = "center";
        proYearCell.innerHTML = product["year"];
        proYearCell.classList.add("pro-year");

        numberCell.style.textAlign = "center";
        numberCell.innerHTML = number;
        numberCell.classList.add("number");

        priceCell.innerHTML = formatter.format(product["price"]);
        priceCell.classList.add("price");

        sumPerProduct = product["price"] * number;
        sumCell.innerHTML = formatter.format(sumPerProduct);
        sumCell.classList.add("sum-price");

        removeLink.innerHTML = '<i class="fa fa-trash" style="color: gray;"></i>';
        removeLink.setAttribute('href', '#');
        removeLink.setAttribute('data-code', window.localStorage.key(i));
        removeLink.onclick = function() {
            removeCart(this.dataset.code, typeOfVIP, isPersonal);
        };
        removeCell.appendChild(removeLink);
        removeCell.classList.add("remove");

        purchaseBtn.innerHTML = 'Đặt Mua';
        purchaseBtn.classList.add("purchase-btn");
        purchaseCell.appendChild(purchaseBtn);

        tr.appendChild(imageCell);
        tr.appendChild(nameCell);
        tr.appendChild(proYearCell);
        tr.appendChild(numberCell);
        tr.appendChild(priceCell);
        tr.appendChild(sumCell);
        tr.appendChild(removeCell);
        tr.appendChild(purchaseCell);
        container.appendChild(tr);
        totalPreTax += sumPerProduct;
    }
    discount = totalPreTax * discountRate;
    tax = (totalPreTax - discount) * taxRate;
    document.getElementById('pre-tax-total').innerHTML = formatter.format(totalPreTax);
    document.getElementById('discount').innerHTML = formatter.format(discount);
    document.getElementById('tax').innerHTML = formatter.format(tax);
    document.getElementById('total').innerHTML = formatter.format(totalPreTax - discount + tax);
}

function removeCart(code, typeOfVIP, isPersonal) {
    if(typeof window.localStorage[code] !== 'undefined') {
        window.localStorage.removeItem(code);
        showCart(typeOfVIP, isPersonal);
    }
}